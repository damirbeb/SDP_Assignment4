package smart_home_remote_control;

public class Stereo {
    public void turnOn() {
        System.out.println("Stereo turned on");
    }

    public void turnOff() {
        System.out.println("Stereo turned off");
    }

    public void setVolume(int volume) {
        System.out.println("Stereo volume set to " + volume);
    }
}